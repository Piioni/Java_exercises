package Unidad7.Composicion.coche.Classes;

public class Llanta {
    private String marca;
    private int diametroRin;
    private int altura;
    private int anchura;


    public Llanta(String marca, int diametroRin, int altura, int anchura) {
        this.marca = marca;
        this.diametroRin = diametroRin;
        this.altura = altura;
        this.anchura = anchura;
    }

}
